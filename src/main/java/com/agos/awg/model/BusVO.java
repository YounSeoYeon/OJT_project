package com.agos.awg.model;

public class BusVO {
	
	private int bus_idx;
	private String bus_code;
	private String bus_nm;
	private String bus_rep;
	private String bus_reg_no;
	private String bus_tel;
	private String bus_fax;
	private String bus_item;
	private String bus_email;
	private String busAddress;
	
	public int getBus_idx() {
		return bus_idx;
	}
	public void setBus_idx(int bus_idx) {
		this.bus_idx = bus_idx;
	}
	public String getBus_code() {
		return bus_code;
	}
	public void setBus_code(String bus_code) {
		this.bus_code = bus_code;
	}
	public String getBus_nm() {
		return bus_nm;
	}
	public void setBus_nm(String bus_nm) {
		this.bus_nm = bus_nm;
	}
	public String getBus_rep() {
		return bus_rep;
	}
	public void setBus_rep(String bus_rep) {
		this.bus_rep = bus_rep;
	}
	public String getBus_reg_no() {
		return bus_reg_no;
	}
	public void setBus_reg_no(String bus_reg_no) {
		this.bus_reg_no = bus_reg_no;
	}
	public String getBus_tel() {
		return bus_tel;
	}
	public void setBus_tel(String bus_tel) {
		this.bus_tel = bus_tel;
	}
	public String getBus_fax() {
		return bus_fax;
	}
	public void setBus_fax(String bus_fax) {
		this.bus_fax = bus_fax;
	}
	public String getBus_item() {
		return bus_item;
	}
	public void setBus_item(String bus_item) {
		this.bus_item = bus_item;
	}
	public String getBus_email() {
		return bus_email;
	}
	public void setBus_email(String bus_email) {
		this.bus_email = bus_email;
	}	
	public String getBusAddress() {
		return busAddress;
	}
	public void setBusAddress(String busAddress) {
		this.busAddress = busAddress;
	}

	
}
